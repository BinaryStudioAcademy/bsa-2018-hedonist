<template>
    <section class="columns">
        <section class="column is-half">
            <template v-for="(place, index) in places">
                <PlacePreviewList
                    v-if="isPlacesLoaded"
                    :key="place.id" 
                    :place="place" 
                    :timer="50 * (index+1)"
                />
            </template>
        </section>

        <section class="column mapbox-wrapper right-side">
            <section id="map">
                <mapbox
                    :access-token="getMapboxToken"
                    :map-options="{
                        style: getMapboxStyle,
                        center: {
                            lat: 40.7128,
                            lng: -74.0060
                        },
                        zoom: 11
                    }"
                    :scale-control="{
                        show: true,
                        position: 'top-left'
                    }"
                    @map-init="mapInitialized"
                    @map-load="mapLoaded"
                />
            </section>
        </section>
    </section>
</template>

<script>
import { mapState } from 'vuex';
import { mapGetters } from 'vuex';
import PlacePreviewList from './PlacePreviewList';
import Mapbox from 'mapbox-gl-vue';
import LocationService from '@/services/location/locationService';
import MarkerService from '@/services/map/markerManagerService';

let markerManager = null;

export default {
    name: 'SearchPlace',
    components: {
        PlacePreviewList,
        Mapbox,
    },
    data() {
        return {
            filterQuery: '',
            isMapLoaded: false,
            isPlacesLoaded: false,
            map: {},
        };
    },
    created() {
        this.$store.dispatch("place/fetchPlaces")
            .then(() => this.isPlacesLoaded = true);
    },
    methods: {
        mapInitialized(map) {
            this.map = map;
            LocationService.getUserLocationData()
                .then(coordinates => {
                    this.jumpTo(coordinates);
                });
        },
        mapLoaded(map) {
            markerManager = new MarkerService(map);
            this.isMapLoaded = true;
        },
        jumpTo (coordinates) {
            this.map.jumpTo({
                center: coordinates,
            });
        },
        updateMap(places){
            markerManager.setMarkers(...places);
        }
    },
    computed: {
        ...mapState('place', ['places']),
        ...mapGetters('map', ['getMapboxToken', 'getMapboxStyle']),
    }
};
</script>

