<template>
    <div class="container">
        <div class="user-reviews-container">
            <div class="user-reviews-header">
                <h3 class="subtitle is-4">{{ userProfile.first_name }}'s recent reviews</h3>
                <div class="header-btn">
                    <a class="button is-info">
                        <span>See all {{ filterAllReviewUser }} reviews</span>
                    </a>
                </div>
            </div>
            <ul class="columns is-variable is-4 is-multiline user-reviews-items">
                <template v-for="place in filteredUsersPlaces">
                    <ReviewsContainerItem
                        :key="place.id"
                        :place="place"
                    />
                </template>
            </ul>
        </div>
    </div>
</template>

<script>
import {mapState, mapActions, mapGetters} from 'vuex';
import ReviewsContainerItem from '@/components/users/ReviewsContainerItem';

export default {
    name: 'ReviewsContainer',
    data() {
        return {
            stopLoading : false
        };
    },
    components: {
        ReviewsContainerItem
    },
    computed: {
        ...mapState('users', {
            places: 'places',
        }),
        ...mapGetters({
            userProfile: 'users/getUserProfile'
        }),
        ...mapGetters('users', ['getReviewsById' , 'getUserReviewsAll']),
        filteredUsersPlaces: function () {
            return this.getReviewsById(parseInt(this.$route.params.id));
        },
        filterAllReviewUser: function () {
            return this.getUserReviewsAll(parseInt(this.$route.params.id)).length;
        },
    },
    methods: {
        ...mapActions({
            fetchAllPlaces: 'users/fetchAllPlaces'
        })
    },
    created() {
        this.$store.dispatch('users/fetchAllPlaces')
            .then(()=>{
                this.$emit('stopLoading', this.stopLoading);
            });
    },

};
</script>
